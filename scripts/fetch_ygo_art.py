"""
Fetch Yu-Gi-Oh! card art from the YGOProDeck API.

For each card in our YGO sets, queries the API for the card ID,
then builds the cropped art URL. Outputs:
1. Patches image_url into card defs (printed to stdout for manual patching)
2. Generates frontend/src/data/ygoCardArt.ts fallback map
"""

import json
import sys
import time
import urllib.request
import urllib.parse
import urllib.error


def fetch_card_id(card_name: str) -> int | None:
    """Query YGOProDeck API for a card's ID."""
    encoded = urllib.parse.quote(card_name)
    url = f"https://db.ygoprodeck.com/api/v7/cardinfo.php?name={encoded}"
    try:
        req = urllib.request.Request(url, headers={'User-Agent': 'Hyperdraft/1.0'})
        with urllib.request.urlopen(req, timeout=10) as resp:
            data = json.loads(resp.read().decode())
            if 'data' in data and len(data['data']) > 0:
                return data['data'][0]['id']
    except (urllib.error.HTTPError, urllib.error.URLError, json.JSONDecodeError, KeyError) as e:
        print(f"  Warning: Could not fetch '{card_name}': {e}", file=sys.stderr)
    return None


def cropped_url(card_id: int) -> str:
    return f"https://images.ygoprodeck.com/images/cards_cropped/{card_id}.jpg"


