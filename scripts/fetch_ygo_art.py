# Cards with names that differ from our code vs the real API
NAME_OVERRIDES = {
    "Kuriboh Token": None,  # Custom card, skip
    "Sangan Scout": None,   # Custom card, skip
    "Tune Warrior": "Tune Warrior",
    "Stardust Warrior": "Stardust Warrior",
    "Utopia Knight": None,  # Custom name
}


def main():
    art_map: dict[str, str] = {}

    print(f"Fetching art for {len(CARD_NAMES)} cards...", file=sys.stderr)

    for name in CARD_NAMES:
        if name in NAME_OVERRIDES and NAME_OVERRIDES[name] is None:
            print(f"  Skipping custom card: {name}", file=sys.stderr)
            continue

        lookup_name = NAME_OVERRIDES.get(name, name)
        card_id = fetch_card_id(lookup_name)

        if card_id:
            url = cropped_url(card_id)
            art_map[name] = url
            print(f"  OK: {name} -> {card_id}", file=sys.stderr)
        else:
            print(f"  MISS: {name}", file=sys.stderr)

        time.sleep(0.15)  # Rate limit

    # Write frontend map
    ts_path = "frontend/src/data/ygoCardArt.ts"
    with open(ts_path, 'w') as f:
        f.write("/**\n * YGO Card Art URLs — auto-generated by scripts/fetch_ygo_art.py\n *\n")
        f.write(" * Cropped art from YGOProDeck API.\n */\n\n")
        f.write("export const YGO_CARD_ART: Record<string, string> = {\n")
        for name, url in sorted(art_map.items()):
            escaped_name = name.replace("'", "\\'")
            f.write(f"  '{escaped_name}': '{url}',\n")
        f.write("};\n")

    print(f"\nWrote {len(art_map)} entries to {ts_path}", file=sys.stderr)

    # Print image_url additions for card defs
    print("\n# Python image_url additions:", file=sys.stderr)
    for name, url in sorted(art_map.items()):
        print(f'    image_url="{url}",')


if __name__ == '__main__':
    main()
